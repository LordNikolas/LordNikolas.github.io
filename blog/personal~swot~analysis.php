            <p>
                <b>1. What are my strengths? What do I do best? What are my positive traits?</b>
            </p>
            <p>
                <i>---> Intrest to dig into the purpose and meaning of everything - Curiosity</i>
            </p>
            <p>
                <b>2. What are my weaknesses? What personality traits may be holding me back in my studies / career?</b>
            </p>
            <p>
                <i>---> Perfectionism, lack of time managment and procrastination.</i>
            </p>
            <p>
                <b>3. How can I turn my strengths / weaknesses into opportunities?</b>
            </p>
            <p>
                <i>---> Perfectionism can expand the depth of my work, while the everlasting curiosity may widen it.
                    All that can effectively make any type of work a piece of art.</i>
            </p>
            <p>
                <b>4. What is threating my ascension? What obstacles do I face to succeed as a student? 
                    Could any of my weaknesses prevent me from succeeding in my studies / career?</b>
            </p>
            <p>
                <i>---> Sometimes I should let go the ideas that I am obsessed with. 
                    I am not certain, perhaps game development is one of them...</i>
            </p>
